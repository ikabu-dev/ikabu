/**
 * 自動締切の期限(closeAt)を持たない募集を掃除するまでの日数。
 *
 * バイト・レイダース・プラベ・別ゲーはスケジュール終了時刻を持たないため closeAt が null。
 * 「明日のプラベ」「明日のビッグラン」を募集するユースケースがあるので、
 * 生きている募集を殺さない十分な長さを取る。
 */
export const RECRUIT_FALLBACK_TTL_DAYS = 7;

/**
 * 1回のスキャンで〆る募集の上限。
 *
 * この機能のデプロイ直後は、7日以上前の closeAt: null の行が一斉にマッチする。
 * 上限を設けないとジョブが詰まり、同じチャンネルの sticky を連打することになる。
 * スキャンは毎分なので、上限を超えた分は次の tick で拾えばよい。
 */
export const RECRUIT_CLOSE_SCAN_LIMIT = 50;
